package Generics;

public class Shuriken {
    private int tamanho;

    //CONSTRUTORES
    public Shuriken(){}
    public Shuriken(int tamanho)
    {
        this.tamanho= tamanho;
    }

    //GETTER
    public int getTamanho()
    {
        return tamanho;
    }

    //SETTER
    public void setTamanho(int tamanho)
    {
        this.tamanho = tamanho;
    }
}
