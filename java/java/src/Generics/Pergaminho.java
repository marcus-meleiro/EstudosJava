package Generics;

public class Pergaminho {
    private String conteudo;

    //CONTRUTORES
    public Pergaminho(){}
    public Pergaminho(String conteudo)
    {
        this.conteudo = conteudo;
    }

    //GETTER
    public String getConteudo()
    {
        return conteudo;
    }

    //SETTER
    public void setConteudo(String conteudo)
    {
        this.conteudo = conteudo;
    }

}
