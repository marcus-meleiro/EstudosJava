package Generics;

public class Kunai {
    private String nome;

    //CONSTRUTORES
    public Kunai(){}
    public Kunai(String nome){
        this.nome = nome;
    }

    //GETTER
    public String getNome() {
        return nome;
    }

    //SETTER
    public void setNome(String nome) {
        this.nome = nome;
    }
}
